def handle_negation(text):
    words = str(text).split()

    new_words = []

    i = 0

    while i < len(words):
        if words[i] in {"not", "no", "never"} and i + 1 < len(words):
            new_words.append(words[i] + "_" + words[i + 1])
            i += 2
        else:
            new_words.append(words[i])
            i += 1

    return " ".join(new_words)
