class HybridEmbeddingMLP(nn.Module):
    def __init__(
        self,
        vocab_size,
        embedding_dim,
        max_len,
        feature_size,
        hidden1,
        hidden2,
        num_classes,
        dropout_rate
    ):
        super().__init__()

        self.embedding = nn.Embedding(
            num_embeddings=vocab_size,
            embedding_dim=embedding_dim,
            padding_idx=0
        )

        embedding_size = max_len * embedding_dim

        input_size = embedding_size

        self.classifier = nn.Sequential(
            nn.Linear(input_size, hidden1),
            nn.BatchNorm1d(hidden1),
            nn.ReLU(),
            nn.Dropout(dropout_rate),

            nn.Linear(hidden1, hidden2),
            nn.BatchNorm1d(hidden2),
            nn.ReLU(),
            nn.Dropout(dropout_rate),

            nn.Linear(hidden2, num_classes)
        )

    def forward(self, x_seq, x_features):
        embedded = self.embedding(x_seq)


        combined = torch.cat((embedded, x_features), dim=1)

        output = self.classifier(combined)

        return output

def train_model(
    X_train_seq,
    X_train_features,
    y_train,
    X_val_seq,
    X_val_features,
    y_val,
    feature_size,
    experiment_name,
    epochs=20,
    batch_size=64,
    lr=0.001,
    weight_decay=1e-4,
    patience=5
):
    embedding_dim = 50
    hidden1 = 256
    hidden2 = 128
    num_classes = 3
    dropout_rate = 0.3

    X_train_seq_tensor = torch.LongTensor(X_train_seq)
    X_val_seq_tensor = torch.LongTensor(X_val_seq)

    X_train_features_tensor = torch.FloatTensor(X_train_features)
    X_val_features_tensor = torch.FloatTensor(X_val_features)

    y_train_tensor = torch.LongTensor(np.array(y_train).astype("int64"))
    y_val_tensor = torch.LongTensor(np.array(y_val).astype("int64"))

    train_dataset = TensorDataset(
        X_train_seq_tensor,
        X_train_features_tensor,
        y_train_tensor
    )

    val_dataset = TensorDataset(
        X_val_seq_tensor,
        X_val_features_tensor,
        y_val_tensor
    )

    train_loader = DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True
    )

    val_loader = DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False
    )

    model = HybridEmbeddingMLP(
        vocab_size=vocab_size,
        embedding_dim=embedding_dim,
        max_len=max_len,
        feature_size=feature_size,
        hidden1=hidden1,
        hidden2=hidden2,
        num_classes=num_classes,
        dropout_rate=dropout_rate
    ).to(device)

    criterion = nn.CrossEntropyLoss()

    optimizer = optim.Adam(
        model.parameters(),
        lr=lr,
        weight_decay=weight_decay
    )

    scheduler = optim.lr_scheduler.StepLR(
        optimizer,
        step_size=5,
        gamma=0.5
    )

    train_losses = []
    val_losses = []
    train_accs = []
    val_accs = []

    best_val_loss = float("inf")
    best_model_state = None
    patience_counter = 0

    for epoch in range(epochs):
        model.train()

        total_train_loss = 0
        correct_train = 0
        total_train = 0

        for batch_seq, batch_features, batch_y in train_loader:
            batch_seq = batch_seq.to(device)
            batch_features = batch_features.to(device)
            batch_y = batch_y.to(device)

            optimizer.zero_grad()

            outputs = model(batch_seq, batch_features)

            loss = criterion(outputs, batch_y)

            loss.backward()

            optimizer.step()

            total_train_loss += loss.item() * batch_y.size(0)

            preds = torch.argmax(outputs, dim=1)

            total_train += batch_y.size(0)

        avg_train_loss = total_train_loss / total_train

        train_acc = correct_train / total_train

        model.eval()

        total_val_loss = 0
        correct_val = 0
        total_val = 0

        with torch.no_grad():
            for batch_seq, batch_features, batch_y in val_loader:
                batch_seq = batch_seq.to(device)
                batch_features = batch_features.to(device)
                batch_y = batch_y.to(device)

                outputs = model(batch_seq, batch_features)

                loss = criterion(outputs, batch_y)

                total_val_loss += loss.item() * batch_y.size(0)

                preds = torch.argmax(outputs, dim=1)

                total_val += batch_y.size(0)

        avg_val_loss = total_val_loss / total_val

        val_acc = correct_val / total_val

        scheduler.step()

        train_losses.append(avg_train_loss)
        val_losses.append(avg_val_loss)
        train_accs.append(train_acc)
        val_accs.append(val_acc)

        print(
            f"{experiment_name} | "
            f"Epoch {epoch + 1}/{epochs} | "
            f"Train Loss: {avg_train_loss:.4f} | "
            f"Val Loss: {avg_val_loss:.4f} | "
            f"Train Acc: {train_acc:.4f} | "
            f"Val Acc: {val_acc:.4f}"
        )

        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            best_model_state = copy.deepcopy(model.state_dict())
            patience_counter = 0
        else:
            patience_counter += 1

        if patience_counter >= patience:
            print("Early stopping triggered.")
            break

    model.load_state_dict(best_model_state)

    history = {
        "train_loss": train_losses,
        "val_loss": val_losses,
        "train_acc": train_accs,
        "val_acc": val_accs
    }

    return model, history

def evaluate_model(
    model,
    X_test_seq,
    X_test_features,
    y_test,
    experiment_name,
    batch_size=64
):
    X_test_seq_tensor = torch.LongTensor(X_test_seq)

    X_test_features_tensor = torch.FloatTensor(X_test_features)

    y_test_tensor = torch.LongTensor(np.array(y_test).astype("int64"))

    test_dataset = TensorDataset(
        X_test_seq_tensor,
        X_test_features_tensor,
        y_test_tensor
    )

    test_loader = DataLoader(
        test_dataset,
        batch_size=batch_size,
        shuffle=False
    )

    model.eval()

    all_preds = []
    all_labels = []

    with torch.no_grad():
        for batch_seq, batch_features, batch_y in test_loader:
            batch_seq = batch_seq.to(device)
            batch_features = batch_features.to(device)

            outputs = model(batch_seq, batch_features)

            preds = torch.argmax(outputs, dim=1)

            all_preds.extend(preds.cpu().numpy())
            all_labels.extend(batch_y.numpy())

    acc = accuracy_score(all_labels, all_preds)

    print(experiment_name)

    print("Test Accuracy:", acc)

    print(
        classification_report(
            all_labels,
            all_preds,
            target_names=list(encoder.classes_),
            zero_division=0
        )
    )

    print("Confusion Matrix:")
    print(confusion_matrix(all_labels, all_preds))

    return acc

