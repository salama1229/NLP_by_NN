    negative_count = sum(1 for word in words if word in negative_words)

    exclamation_count = text.count("!")

    question_count = text.count("?")

    review_length = len(words)

    return [
        positive_count,
        negative_count,
        exclamation_count,
        question_count,
        review_length
    ]

