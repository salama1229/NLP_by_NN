def basic_clean_text(text):
    # 1. lowercase
    text = text.lower()

    # 2. remove urls
    text = re.sub(r"http\S+|www\S+", "", text)

    # 3. remove punctuation
    text = re.sub(f"[{re.escape(string.punctuation)}]", " ", text)

    # 4. remove numbers
    text = re.sub(r"\d+", "", text)

    # 5. remove extra spaces
    text = re.sub(r"\s+", " ", text).strip()

    return text

